                <p class="subheader">Do vaccines cause autism?</p>
                    <p class="normal-text">The widespread fear that vaccines increase risk of autism originated with a 1997 study published by Andrew Wakefield, a British surgeon. The article was published in The Lancet, a prestigious medical journal, suggesting that the measles, mumps, rubella (MMR) vaccine was increasing autism in British children.
                    </p>
                    <p class="normal-text">The paper has since been completely discredited due to serious procedural errors, undisclosed financial conflicts of interest, and ethical violations. Andrew Wakefield lost his medical license and the paper was retracted from The Lancet.
                    </p>
                    <p class="normal-text">Nonetheless, the hypothesis was taken seriously, and several other major studies were conducted. None of them found a link between any vaccine and the likelihood of developing autism.
                    </p>
                    <p class="normal-text">Today, the true causes of autism remain a mystery, but to the discredit of the autism-vaccination link theory, several studies have now identified symptoms of autism in children well before they receive the MMR vaccine. And even more recent research provides evidence that autism develops in utero, well before a baby is born or receives vaccinations.
                    </p>
                <p class="subheader">Can infant immune systems handle so many vaccines?</p>
                    <p class="normal-text">Infant immune systems are stronger than you might think. Based on the number of antibodies present in the blood, a baby would theoretically have the ability to respond to around 10,000 vaccines at one time. Even if all 14 scheduled vaccines were given at once, it would only use up slightly more than 0.1% of a baby’s immune capacity. And scientists believe this capacity is purely theoretical. The immune system could never truly be overwhelmed because the cells in the system are constantly being replenished. In reality, babies are exposed to countless bacteria and viruses every day, and immunizations are negligible in comparison.
                    </p>
                    <p class="normal-text">Though there are more vaccinations than ever before, today’s vaccines are far more efficient. Small children are actually exposed to fewer immunologic components overall than children in past decades.
                    </p>
                <p class="subheader">Is natural immunity is better than vaccine-acquired immunity?</p>                    
                    <p class="normal-text">In some cases, natural immunity — meaning actually catching a disease and getting sick– results in a stronger immunity to the disease than a vaccination. However, the dangers of this approach far outweigh the relative benefits. If you wanted to gain immunity to measles, for example, by contracting the disease, you would face a 1 in 500 chance of death from your symptoms. In contrast, the number of people who have had severe allergic reactions from an MMR vaccine, is less than one-in-one million.
                    </p>
                <p class="subheader">Do vaccines contain unsafe toxins?</p>                    
                    <p class="normal-text">People have concerns over the use of formaldehyde, mercury or aluminum in vaccines. It’s true that these chemicals are toxic to the human body in certain levels, but only trace amounts of these chemicals are used in FDA approved vaccines. In fact, according to the FDA and the CDC, formaldehyde is produced at higher rates by our own metabolic systems and there is no scientific evidence that the low levels of this chemical, mercury or aluminum in vaccines can be harmful. See section III of this guide to review safety information about these chemicals and how they are used in vaccines.
                    </p>
                <p class="subheader">Are better hygiene and sanitation actually responsible for decreased infections, not vaccines?</p>                    
                    <p class="normal-text">Vaccines don’t deserve all the credit for reducing or eliminating rates of infectious disease. Better sanitation, nutrition, and the development of antibiotics helped a lot too. But when these factors are isolated and rates of infectious disease are scrutinized, the role of vaccines cannot be denied.
                    </p>
                    <p class="normal-text">One example is measles in the United States. When the first measles vaccine was introduced in 1963, rates of infection had been holding steady at around 400,000 cases a year. And while hygienic habits and sanitation didn’t change much over the following decade, the rate of measles infections dropped precipitously following the introduction of the vaccine, with only around 25,000 cases by 1970. Another example is Hib disease. According to CDC data, the incidence rate for this malady plummeted from 20,000 in 1990 to around 1,500 in 1993, following the introduction of the vaccine.
                    </p>                    
                <p class="subheader">Are vaccines worth the risk?</p>                    
                    <p class="normal-text">Immediate danger from vaccines is only in the form of allergic reactions or severe side effects. The incidence of death for these side effects are so rare they can’t even truly be calculated. For example, only one death was reported to the CDC between 1990 and 1992 that was attributable to a vaccine. The overall incidence rate of severe allergic reaction to vaccines is usually placed around one case for every one or two million injections.
                    </p>
                <p class="subheader">Can vaccines infect me with the disease it’s trying to prevent?</p>                    
                    <p class="normal-text">Vaccines can cause mild symptoms resembling those of the disease they are protecting against. A common misconception is that these symptoms signal infection. In fact, in the small percentage (less than 1 in one million cases) where symptoms do occur, the vaccine recipients are experiencing a body’s immune response to the vaccine, not the disease itself. There is only one recorded instance in which a vaccine was shown to cause disease. This was the Oral Polio Vaccine (OPV) which is no longer used in the U.S. Since then, vaccines have been in safe use for decades and follow strict Food and Drug Administration (FDA) regulations.
                    </p>
